def has_batchim(word):
    """한글 단어의 마지막 글자에 받침이 있는지 확인"""
    if not word:
        return False
    last = word[-1]
    if '가' <= last <= '힣':
        return (ord(last) - 0xAC00) % 28 != 0
    # 한글이 아니면(영문 등) 받침 없는 것으로 처리
    return False

def apply_josa(text, country_name):
    """{c}가/{c}이, {c}는/{c}은 등 조사를 받침에 맞게 자동 처리"""
    batchim = has_batchim(country_name)
    # {c}+조사 패턴 치환 (받침 있으면 앞 조사, 없으면 뒤 조사)
    replacements = [
        ('{c}가', '{c}이' if batchim else '{c}가'),
        ('{c}는', '{c}은' if batchim else '{c}는'),
        ('{c}를', '{c}을' if batchim else '{c}를'),
        ('{c}와', '{c}과' if batchim else '{c}와'),
        ('{c}로', '{c}으로' if batchim else '{c}로'),
    ]
    for pat, rep in replacements:
        text = text.replace(pat, rep)
    return text.replace('{c}', country_name)

def load_prev_prices():
    path = os.path.join(BASE, 'prices.json')
    if os.path.exists(path):
        with open(path, encoding='utf-8') as f:
            return json.load(f)
    return None

